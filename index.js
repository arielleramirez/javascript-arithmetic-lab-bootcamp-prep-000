function add(x, y) {
  var product = x + y;
  return product;
}  

function subtract(x,y){
  var product = x - y;
  return product;
}

function multiply(x,y){
  var product = x * y;
  return product;
}

function divide(x,y){
  var product = x / y;
  return product;
}

function inc(n){
  var product = n + 1;
  return product;
}

function dec(n){
  var product = n - 1;
  return product;
}